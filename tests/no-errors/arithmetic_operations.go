package main

import "fmt"

/*
 * Simple program that prints basic arithmetic operations
 */
func main() {

	var add = 1 + 2
	var mult = 5 * 2
	var sub = 10 - 2
	var div = 20 / 2
	var rem = 4 % 2

	fmt.Println("add = 1 + 2 =", add)
	fmt.Println("mult = 5 * 2 =", mult)
	fmt.Println("sub = 10 - 2 =", sub)
	fmt.Println("div = 20 / 2 =", div)
	fmt.Println("rem = 4 % 2 =", rem)
}
